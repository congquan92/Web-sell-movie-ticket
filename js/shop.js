//radio-search
function mangproduct_radio(radio, arr) {
  let mang = [];
  for (let i = 0; i < arr.length; i++) {
    // Kiểm tra nếu nametag của đối tượng trong mảng trùng với radio đã chọn
    if (arr[i].nametag == radio) {
      mang.push(arr[i]);
    }
  }
  return mang;
}

